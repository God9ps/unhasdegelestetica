                <h4 style="color: black">As extensões de cílios ou pestanas são uma alternativa viável para obter umas pestanas mais longas, mas
                    aqui algumas existem coisas para saber antes de você optar pela sua extensão!</h4>
                <h3 style="color: black">Como funcionam as extensões de pestanas?</h3>
                <h4 style="color: black">Pestanas individuais são mergulhados em um adesivo de grau médico e ligados ao seu pêlo natural. Este
                    processo é feito pêlo a pêlo, o que faz com que seja um processo demorado.</h4>
                <h4 style="color: black">O adesivo pode durar entre 4 e 6 semanas, dependendo de quão cuidadosa você seja (quanto mais você
                    esfrega os olhos, lave o rosto, etc., mais rápido o adesivo se desgastará). Se você receber retoques
                    regulares (re-aplicação do adesivo), as pestanas podem durar mais tempo.</h4>
                <h4 style="color: black"> As extensões de pestanas podem ser personalizadas. Você pode escolher a cor, a textura e o comprimento
                    do pêlo, então, se você quer um visual mais natural ou um olhar ultra-glamuroso, a extensão de pestanas
                    é sem duvida uma boa opção!</h4>
                <h3 style="color: black">Efeitos colaterais</h3>
                <h4 style="color: black">As duas principais preocupações ao obter extensões de pestanas são a reação ao adesivo e à ruptura.
                    Algumas mulheres podem experimentar uma reação alérgica ou sensibilidade ao adesivo usado para fixar os
                    pêlos. Daí a preocupação da nossa especialista em certificar-se antecipadamente se você tem alguma
                    alergia. Além disso, as extensões frequentes podem levar à quebra da pestana natural.</h4>

                <h3 style="color: black">Outras coisas a considerar</h3>
                <h4 style="color: black"> Quanto tempo dura o adesivo dependente de quão cuidadosa você é com as extensões. Esfregar os olhos, a
                    lavagem do rosto, os removedores de maquiagem à base de óleo e o rímel podem desgastar o adesivo,
                    fazendo com que as pestanas caiam com maior facilidade exigindo mais retoques.</h4>
                <h4 style="color: black">Temos de admitir que as extensões de pestanas são otimas e com um resultado final excelente, e hoje em
                    dia não são só para as carteiras mais recheadas uma vez que já consegue as suas extensões por um preço
                    bem acessível e conseguir assim o olhar irresistivel que tanto procura.
